                        <h3 style="font-size: 18px; color: #333; margin-bottom: 15px;" itemprop="name">什么是ASPICE认证？</h3>
                        <div itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
                          <div style="color: #666; line-height: 1.8;" itemprop="text">
                            ASPICE（Automotive SPICE）是汽车软件过程改进及能力评定的国际标准，基于ISO/IEC 330xx系列标准，专门针对汽车行业的软件开发过程。它由德国汽车工业联合会（VDA）质量管理中心（QMC）运营发展，被广泛应用于汽车OEM厂商对供应商的研发能力评估。ASPICE评估等级从0级到5级，大多数汽车厂商要求供应商达到ASPICE CL2或CL3级别。
                          </div>
                        </div>
                      </div>

